// Helper functions for filtering and searching
export function getFightersByWeightClass(weightClass: string): AppFighter[] {
  return fallbackFighters.filter(fighter => 
    fighter.weightClass.toLowerCase().includes(weightClass.toLowerCase())
  );
}

export function searchFighters(query: string): AppFighter[] {
  const searchTerm = query.toLowerCase();
  return fallbackFighters.filter(fighter => 
    fighter.name.toLowerCase().includes(searchTerm) ||
    (fighter.nickname && fighter.nickname.toLowerCase().includes(searchTerm)) ||
    fighter.weightClass.toLowerCase().includes(searchTerm) ||
    fighter.nationality.toLowerCase().includes(searchTerm)
  );
}

export function getFighterById(id: string): AppFighter | undefined {
  return fallbackFighters.find(fighter => fighter.id === id);
}

export function getChampions(): AppFighter[] {
  return fallbackFighters.filter(fighter => fighter.rank === "#1");
}

export function getFightersByRank(rank: string): AppFighter[] {
  return fallbackFighters.filter(fighter => fighter.rank === rank);
}

export function getTopFighters(limit: number = 123): AppFighter[] {
  return fallbackFighters.slice(0, limit);
}

// Get fighters by gender
export function getMensFighters(): AppFighter[] {
  return fallbackFighters.filter(fighter => !fighter.weightClass.toLowerCase().includes("women"));
}

export function getWomensFighters(): AppFighter[] {
  return fallbackFighters.filter(fighter => fighter.weightClass.toLowerCase().includes("women"));
}

// Get division statistics
export function getDivisionStats() {
  const divisions = [...new Set(fallbackFighters.map(f => f.weightClass))];
  return divisions.map(division => ({
    name: division,
    fighterCount: getFightersByWeightClass(division).length,
    champion: getChampions().find(c => c.weightClass === division)?.name || "Vacant"
  }));
}

// Get male division stats
export function getMaleDivisionStats() {
  const maleFighters = getMensFighters();
  const divisions = [...new Set(maleFighters.map(f => f.weightClass))];
  return divisions.map(division => ({
    name: division,
    fighterCount: getFightersByWeightClass(division).length,
    champion: getChampions().find(c => c.weightClass === division)?.name || "Vacant"
  }));
}
